# 3 рядку 3 колонку - "А", 11 рядок 3 колонка - "_"
# 1/12 window height
SIZE = 600 / 12
# A - wall, "-" - space
# row - рядок
def make_map():
    result = []
    for row in range(len(map)): # Для кожного зі списків
        for column in range(len(map[row])): # Для кожного елемента списку
            if map[row][column] == "A": # Якщо елемент - списку це A
                x = SIZE * (column + 2)
                y = SIZE * row
                # import pygame не забудьте
                result.append(pygame.Rect(x, y, SIZE, SIZE))
    return result

